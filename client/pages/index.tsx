import Head from "next/head";
import { analyzeFile } from "@/utils/apiUtils";
import { useMutation } from "react-query";
import { useState } from "react";
import FileUploader from "@/components/FileUploader"

const url = "/api/upload";

interface ContainerProps {
  children: React.ReactNode
}

const Container = ({ children }: ContainerProps) => (
  <div className="flex flex-col items-center justify-between gap-4 min-h-60 bg-zinc-800 w-full max-w-2xl py-10 px-4 rounded-xl h-fit">
    {children}
  </div>
)

export default function Home() {
  const [chordsUsed, setChordsUsed] = useState<string[] | undefined>(undefined);

  const { mutate: analyzeFileMutation } = useMutation(
    async () => analyzeFile(/*File*/),
    {
      onSuccess: (data: any) => {
        console.log(data);
        setChordsUsed(data.chords);
      },
      onError: (error: any) => {
        console.error(error);
      },
    }
  );

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container>
        <h1 className="text-2xl font-bold">File Uploader</h1>
        <FileUploader
          url={url}
          acceptedFileTypes={[
            "image/png",
            "image/jpeg",
          ]}
          maxFileSize={100}
          label="Max File Size: 1MB"
          labelAlt="Accepted File Types: png, jpeg"
        />
      </Container>
      <Container>
        <h1 className="text-2xl font-bold">File Uploader</h1>
        <FileUploader
          url={url}
          acceptedFileTypes={[
            "image/png",
            "image/jpeg",
          ]}
          allowMultiple={true}
          maxFileSize={100}
          label="Max File Size: 100MB (multiple)"
          labelAlt="Accepted File Types: png, jpeg"
        />
      </Container>
      <Container>
        <h1 className="text-2xl font-bold">File Uploader</h1>
        <FileUploader
          url={'https://example.com'}
          acceptedFileTypes={[
            "image/png",
            "image/jpeg",
          ]}
          allowMultiple={true}
          maxFileSize={100}
          label="Max File Size: 100MB (non-existent endpoint)"
          labelAlt="Accepted File Types: png, jpeg"
        />
      </Container>
      <div>
        <button onClick={() => analyzeFileMutation()}>upload music!</button>
        <div>
          {chordsUsed && (
            <div>
              <h2>Chords used:</h2>
              <ul>
                {chordsUsed.map((chord) => (
                  <li key={chord}>{chord}</li>
                ))}
              </ul>
            </div>
          )}
        </div>
      </div>
    </>
  );
}
